# Método de tupla count() 
tupla1 = (1,1,2,2,3,3,4,4,4,5,5,5,6,6,6)
print("tupla1:", tupla1)
print("tupla1.count(1):", tupla1.count(1))  # cuenta los nº 4 existentes
print("tupla1.count(2):", tupla1.count(2))  # cuenta los nº 4 existentes
print("tupla1.count(4):", tupla1.count(4))  # cuenta los nº 4 existentes
print("tupla1.count(5):", tupla1.count(5))  # cuenta los nº 4 existentes
print("tupla1.count(6):", tupla1.count(6))  # cuenta los nº 4 existentes

