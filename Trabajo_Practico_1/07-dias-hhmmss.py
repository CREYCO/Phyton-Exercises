# Pida al usuario un número x de días y luego mostrar por pantalla cuántas horas, minutos y
# segundos

dias=int(input("Dias? :"))

horas=dias * 24
minutos=horas * 60
segundos=minutos * 60

print("dias" , dias)
print("Horas" , horas )
print("minutos" , minutos)
print("segundos" ,segundos)

