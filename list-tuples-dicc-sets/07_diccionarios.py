# Creación de un diccionario vacío 1
dict1 = {}
print("dict1:", dict1)

dict2 = dict()
print("dict2:", dict2)

# Diccionario con claves enteras
dict1 = {1: 'Notebook', 2: 'PC', 3: 'Tablet', 4: 'Smartphone'}
print("Diccionario con claves enteras:", dict1)
  
# diccionario con claves string
dict2 = {'Lara': 1, 'Cristina': 71, 'Gise': 36}
print("Diccionario con claves string", dict2)
print("dict2['Gise']:", dict2['Gise'])

#Diccionario con claves de distinto tipo
dict3 = {'Nombre': 'Nacho', 1: [1, 2, 3, 4]}
print("Diccionario con claves de distinto tipo:", dict3)

#Diccionario español - inglés
diccionario = {'uno': 'one', 'dos': 'two', 'tres': 'three'}
print("dicionario['dos']:",diccionario['dos'])
